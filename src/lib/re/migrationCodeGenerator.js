/**
 * Generate migration code artifacts from scan + blueprint.
 * @returns {{ phases, files, summary, generatedAt, options }}
 */
export function generateMigrationCode(scan, blueprint, options = {}) {
  const targetId = options.targetStack ?? blueprint.targetStack
  const target = getMigrationTarget(targetId)
  const family = getTargetFamily(targetId)
  const effectiveBlueprint = {
    ...blueprint,
    targetStack: target.id,
    targetLabel: target.label,
    targetDescription: target.description,
  }

  const scope = options.scope ?? 'p0'
  const moduleLimit = scope === 'scaffold' ? 0 : scope === 'p0' ? 2 : blueprint.backlog.length
  const opts = {
    packageBase: options.packageBase ?? 'com.horizon.migration',
    moduleLimit,
    scope,
    targetStack: target.id,
  }

  const backlogSlice = blueprint.backlog.slice(0, moduleLimit)
  let files = []

  if (family === 'jvm') {
    files.push(...generateSpringScaffold(scan, effectiveBlueprint, opts))
    files.push(...generateSpringContracts(scan, effectiveBlueprint, opts))
    backlogSlice.forEach((item) => files.push(...generateSpringModule(scan, item, effectiveBlueprint, opts)))
    if (moduleLimit > 0) {
      files.push(...generateSpringIntegration(scan, effectiveBlueprint, opts))
      files.push(...generateSpringTests(scan, effectiveBlueprint, opts))
    }
  } else if (family === 'dotnet') {
    files.push(...generateDotNetScaffold(scan, effectiveBlueprint, opts))
    files.push(...generateSpringContracts(scan, effectiveBlueprint, opts))
    backlogSlice.forEach((item) => files.push(...generateDotNetModule(scan, item, effectiveBlueprint)))
    if (moduleLimit > 0) files.push(generateHarnessTasks(scan, effectiveBlueprint, backlogSlice, target.label))
  } else if (family === 'python') {
    files.push(...generateFastApiScaffold(scan, effectiveBlueprint))
    backlogSlice.forEach((item) => files.push(...generateFastApiModule(item)))
    if (moduleLimit > 0) files.push(generateHarnessTasks(scan, effectiveBlueprint, backlogSlice, target.label))
  } else {
    files.push(...generateFrontendScaffold(scan, effectiveBlueprint, target))
    files.push(...generateSpringContracts(scan, effectiveBlueprint, opts))
    backlogSlice.forEach((item) => files.push(...generateFrontendModule(scan, item, effectiveBlueprint, target)))
    if (moduleLimit > 0) files.push(generateHarnessTasks(scan, effectiveBlueprint, backlogSlice, target.label))
  }

  const phases = CODEGEN_PHASES.map((p) => ({
    ...p,
    status: files.some((f) => f.phase === p.id) ? 'complete' : moduleLimit === 0 && p.id !== 'scaffold' ? 'skipped' : 'pending',
    fileCount: files.filter((f) => f.phase === p.id).length,
  }))

  return {
    scanId: scan.id,
    targetStack: target.id,
    blueprintTarget: target.label,
    generatedAt: new Date().toISOString(),
    phases: CODEGEN_PHASES,
    phaseResults: phases,
    files,
    options: opts,
    summary: `Generated ${files.length} files across ${phases.filter((p) => p.fileCount > 0).length} phases for ${backlogSlice.length} module(s) → ${target.label}`,
  }
}

export function getCodegenPhases() {
  return CODEGEN_PHASES
}

export function buildCodeBundleExport(result) {
  const header = `# TCS Migration Code Bundle
# Scan: ${result.scanId}
# Target: ${result.blueprintTarget}
# Generated: ${result.generatedAt}
# Files: ${result.files.length}

`
  const body = result.files
    .map((f) => `\n${'='.repeat(60)}\nFILE: ${f.path}\nPHASE: ${f.phase} | MODULE: ${f.module}\n${'='.repeat(60)}\n${f.content}`)
    .join('\n')
  return header + body
}

